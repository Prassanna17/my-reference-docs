package com.selenium.automation;

public class PolicyFundsPojo {
	
	private String policyNumber;
	private String sourceFunds;
	private String targetFund;
	
	public String getPolicyNumber() {
		return policyNumber;
	}
	public void setPolicyNumber(String policyNumber) {
		this.policyNumber = policyNumber;
	}
	public String getSourceFunds() {
		return sourceFunds;
	}
	public void setSourceFunds(String sourceFunds) {
		this.sourceFunds = sourceFunds;
	}
	public String getTargetFund() {
		return targetFund;
	}
	public void setTargetFund(String targetFund) {
		this.targetFund = targetFund;
	}	
}
